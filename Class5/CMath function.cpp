#include <iostream>
#include <cmath>

using namespace std;

int main (){
    cout << sqrt(1600)<< endl; // squareroot
    cout << ceil(3.1)<< endl; // rounding up
    cout << floor(3.1) << endl; // rounding down
    cout << cos(0) << endl; // cosine
    cout << exp(2) << endl; //power of enviourmental number
    cout << fabs(-4.6) << endl; // Absolute
    cout << pow(2,3)<< endl; // in the Power of, 2 in the power of 3; 2^3
    cout << log10(10) << endl; // log based 10
    cout << log(10) << endl; // log based e

    return 0;
}